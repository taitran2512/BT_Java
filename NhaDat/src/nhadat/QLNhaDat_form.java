    private void btn_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuActionPerformed
        // TODO add your handling code here:
        if (combo_loainhap.getSelectedItem().toString().equals("Nhà")) {
            Nha nha = new Nha();
            nha.setMa_gd(tf_maso.getText());
            SimpleDateFormat time = new SimpleDateFormat("dd/MM/yyyy");
            try {
                nha.setNgay_gd(time.parse(tf_ngaygd.getText()));
            } catch (Exception e) {
            }
            nha.setDongia(Integer.parseInt(tf_dongia.getText()));
            nha.setLoainha(combo_loainha.getSelectedItem().toString());
            nha.setDiachi(tf_diachi.getText());
            nha.setDientich(Integer.parseInt(tf_dientich.getText()));
            if (!listnhadat.contains(nha)) {
                listnhadat.add(nha);
                ta_hienthi.setText("lưu thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Trùng mã số", "thong bao", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            Dat dat = new Dat();
            dat.setMa_gd(tf_maso.getText());
            SimpleDateFormat time = new SimpleDateFormat("dd/mm/yyyy");
            try {
                dat.setNgay_gd(time.parse(tf_ngaygd.getText()));
            } catch (Exception e) {
            }
            dat.setDongia(Integer.parseInt(tf_dongia.getText()));
            dat.setDientich(Integer.parseInt(tf_dientich.getText()));
            dat.setLoaidat(comno_loaidat.getSelectedItem().toString());
            if (!listnhadat.contains(dat)) {
                listnhadat.add(dat);
                ta_hienthi.setText("lưu thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Trùng mã số", "thong bao", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_luuActionPerformed

