    private void btn_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuActionPerformed
        // TODO add your handling code here:
        if (combo_loaikhach.getSelectedItem().toString().equals("Khách Việt Nam")) {
            KhachVn vn = new KhachVn();
            vn.setMaso(tf_maso.getText());
            vn.setHoten(tf_hoten.getText());
            SimpleDateFormat time = new SimpleDateFormat("dd/MM/yyyy");
            try {
                vn.setNgayrahd(time.parse(tf_date.getText()));
            } catch (Exception e) {
            }
            vn.setSo_kw(Integer.parseInt(tf_kw.getText()));
            vn.setDongia(Integer.parseInt(tf_dongia.getText()));
            vn.setLoaikh(combo_doituong.getSelectedItem().toString());
            vn.setDinhmuc(Integer.parseInt(tf_dinhmuc.getText()));
            if (!listTienDien.contains(vn)) {
                listTienDien.add(vn);
                ta_hienthi.setText("lưu thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Trùng mã số", "thong bao", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            KhachNgoai nn = new KhachNgoai();
            nn.setMaso(tf_maso.getText());
            nn.setHoten(tf_hoten.getText());
            SimpleDateFormat time = new SimpleDateFormat("dd/MM/yyyy");
            try {
                nn.setNgayrahd(time.parse(tf_date.getText()));
            } catch (Exception e) {
            }
            nn.setSo_kw(Integer.parseInt(tf_kw.getText()));
            nn.setDongia(Integer.parseInt(tf_dongia.getText()));
            nn.setQuocgia(tf_quocgia.getText());
            if (!listTienDien.contains(nn)) {
                listTienDien.add(nn);
                ta_hienthi.setText("lưu thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Trùng mã số", "thong bao", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_luuActionPerformed

