    private void btn_kw500ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kw500ActionPerformed
        // TODO add your handling code here:
        this.ta_hienthi.setText("");
        for (QlTienDien qlTienDien : listTienDien) {
            if (qlTienDien.getClass().getSimpleName().equals("KhachVn")) {
                if (qlTienDien.getSo_kw() - ((KhachVn) qlTienDien).getDinhmuc() > 500) {
                    this.ta_hienthi.append("Hóa đơn có kw lớn hơn định mức 500kw: " + "\n" + qlTienDien.toString());
                }
            }
        }


    }//GEN-LAST:event_btn_kw500ActionPerformed
    private void btn_nhohondinhmucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_nhohondinhmucActionPerformed
        // TODO add your handling code here:
        this.ta_hienthi.setText("");
        for (QlTienDien qlTienDien : listTienDien) {
            if (qlTienDien.getClass().getSimpleName().equals("KhachVn")) {
                if (((KhachVn) qlTienDien).getDinhmuc() > qlTienDien.getSo_kw()) {
                    this.ta_hienthi.append("Hóa đơn có kw nhỏ hơn định mức: " + "\n" + qlTienDien.toString());
                }
            }
        }
    }//GEN-LAST:event_btn_nhohondinhmucActionPerformed

    private void btn_searchbyyearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchbyyearActionPerformed
        // TODO add your handling code here:
        Date date = null;
        SimpleDateFormat time = new SimpleDateFormat("MM/yyyy");
        try {
            date = time.parse(tf_date.getText());
        } catch (Exception e) {
        }
        this.ta_hienthi.setText("");
        for (QlTienDien qlTienDien : listTienDien) {
            if (date.getMonth() == (qlTienDien.getNgayrahd()).getMonth() && date.getYear() == (qlTienDien.getNgayrahd()).getYear()) {
                this.ta_hienthi.append(qlTienDien.toString());
            }
        }
    }//GEN-LAST:event_btn_searchbyyearActionPerformed

