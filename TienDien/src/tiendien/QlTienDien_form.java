    private void btn_soluongtungKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_soluongtungKHActionPerformed
        // TODO add your handling code here:
        int sl_VN = 0;
        int sl_NN = 0;
        for (QlTienDien qlTienDien : listTienDien) {
            if (qlTienDien.getClass().getSimpleName().equals("KhachVn")) {
                sl_VN++;
            } else {
                sl_NN++;
            }
        }
        ta_hienthi.setText("Số lượng khách Vn: " + sl_VN + "\n" + "Số lượng khách NN: " + sl_NN);

    }//GEN-LAST:event_btn_soluongtungKHActionPerformed

    private void btn_TTnuocngoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TTnuocngoaiActionPerformed
        // TODO add your handling code here:
        double thanhtien = 0;
        int sl = 0;
        for (QlTienDien qlTienDien : listTienDien) {
            if (qlTienDien.getClass().getSimpleName().equals("KhachNgoai")) {
                thanhtien += qlTienDien.ThanhTien();
                sl++;
            }
        }
        ta_hienthi.setText("trung bình thành tiền khách nước ngoài: " + (thanhtien / sl));
    }//GEN-LAST:event_btn_TTnuocngoaiActionPerformed

    private void btn_hdlonhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hdlonhatActionPerformed
        // TODO add your handling code here:
        Collections.sort(listTienDien, new Comparator<QlTienDien>() {
            @Override
            public int compare(QlTienDien o1, QlTienDien o2) {
                return o1.ThanhTien() > o2.ThanhTien() ? 1
                        : o1.ThanhTien() < o2.ThanhTien() ? -1 : 0;
            }
        });
        int i = listTienDien.size() - 1;
        ta_hienthi.setText("Khách hàng có hóa đơn lớn nhất: " + listTienDien.get(i));
    }//GEN-LAST:event_btn_hdlonhatActionPerformed

