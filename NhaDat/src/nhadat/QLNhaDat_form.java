    private void btn_gd92013ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_gd92013ActionPerformed
        // TODO add your handling code here:
        ta_hienthi.setText("");
        for (QLNhaDat nhaDat : listnhadat) {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            String str = sdf.format(nhaDat.getNgay_gd());
            String[] dateGiaoDichDat = str.split("/");
            if (dateGiaoDichDat[1].equals("09") && dateGiaoDichDat[2].equals("2013")) {
                ta_hienthi.append(nhaDat.toString() + "\n");
            }

        }
    }//GEN-LAST:event_btn_gd92013ActionPerformed

